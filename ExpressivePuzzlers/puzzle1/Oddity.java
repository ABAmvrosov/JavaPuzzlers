package net.aab.JavaPuzzlers.ExpressivePuzzlers.puzzle1;

public class Oddity {
    public static boolean isOdd(int i) {
        return i % 2 == 1;
    }

    public static void main(String[] args) {
        System.out.println(-11 / 2);
        System.out.println(11 / -2);
        System.out.println(11 % -2);
        System.out.println(-11 % 2);
    }
}
